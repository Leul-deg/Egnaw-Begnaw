import mongoose from 'mongoose';

export const OrganizerSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
        match: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
    }
});

export const Organizer = mongoose.model('Organizer', OrganizerSchema);